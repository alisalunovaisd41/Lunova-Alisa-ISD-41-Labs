#include <iostream>
#include <cmath>  // for sin(), fabs()

using namespace std;

int main() {
    double x, y;
    //аби мати саме для п'яти введених значень
    for (int i = 0; i < 5; i++) {
        cout << "Enter value of x: ";
        cin >> x;

        // Формула y = sin^5(x) + |5x - 1.5|
        y = pow(sin(x), 5) + fabs(5 * x - 1.5);

        cout << "For x = " << x << ", y = " << y << endl;
    }

    return 0;
}

