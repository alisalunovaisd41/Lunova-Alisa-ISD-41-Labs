#include <iostream>
using namespace std;

int main() {
    int sum = 0; // Змінна для зберігання суми непарних чисел

    // Цикл від 1 до 100
    for (int i = 1; i <= 100; i++) {
        // Перевіряємо, чи число непарне
        if (i % 2 != 0) {
            sum += i; // Додаємо непарне число до суми
        }
    }

    // Виводимо результат
    cout << "Сума непарних чисел від 1 до 100 дорівнює: " << sum << endl;

    return 0;
}

