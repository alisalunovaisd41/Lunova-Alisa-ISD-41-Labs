#include <iostream>
#include <cmath> // Для використання pow
using namespace std;

int main() {
    double a, b, h;

    // Введення значень a, b та h
    cout << "Введіть значення a: ";
    cin >> a;
    cout << "Введіть значення b: ";
    cin >> b;
    cout << "Введіть значення h: ";
    cin >> h;

    // Перевірка на коректність
    if (h <= 0) {
        cout << "Крок h має бути додатнім." << endl;
        return 1; // Завершуємо програму з кодом помилки
    }

    // Виведення заголовка таблиці
    cout << "-------------------" << endl;
    cout << ": X : Y :" << endl;
    cout << "-------------------" << endl;

    // Табулювання функції
    for (double x = a; x <= b; x += h) {
        // Перевірка на допустимість x (не менше 0, оскільки корінь з від'ємного числа неможливий)
        if (x < 0) {
            cout << "Значення x = " << x << " не належить ОДЗ." << endl;
            break; // Вихід з циклу, якщо x не в ОДЗ
        }

        // Обчислення y
        double y = pow(x, 3.0 / 4.0) + 6;

        // Виведення значень x та y
        cout << ": " << x << " : " << y << " :" << endl;
    }

    cout << "-------------------" << endl;
    return 0;
}
