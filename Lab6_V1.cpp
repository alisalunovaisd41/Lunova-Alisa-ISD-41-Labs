#include <iostream>
using namespace std;

int main() {
    int N;
    cout << "Введіть натуральне число N: ";
    cin >> N;

    // Перевірка, чи є N натуральним числом
    if (N <= 0) {
        cout << "Будь ласка, введіть натуральне число." << endl;
        return 1; // Завершення програми з кодом помилки
    }

    int product = 1; // Ініціалізуємо добуток
    bool hasEvenDigit = false; // Прапор для перевірки наявності парних цифр

    while (N > 0) {
        int digit = N % 10; // Отримуємо останню цифру
        N /= 10; // Видаляємо останню цифру

        // Перевірка, чи цифра парна
        if (digit % 2 == 0) {
            product *= digit; // Додаємо парну цифру до добутку
            hasEvenDigit = true; // Встановлюємо прапор на true
        }
    }

    // Виведення результату
    if (hasEvenDigit) {
        cout << "Добуток парних цифр: " << product << endl;
    } else {
        cout << "Парних цифр не знайдено." << endl;
    }

    return 0;
}

